package Login;

public class CartBean {
	
	private String user;
	private String updateid;
	private String updateType;
	private String serialNo;
	private String status;
	private String enggCode;
	private String ROCode;
	
	
	public String getUser() 
	  { return user; 
	  }
	  public void setUser(String newUser) 
	  { user = newUser; 
	  } 
	  
	  public String getUpdateId() 
	  { return updateid; 
	  }
	  public void setUpdateId(String newUpdateId) 
	  { updateid = newUpdateId; 
	  }
	  
	  public String getUpdateType() 
	  { return updateType; 
	  }
	  public void setUpdateType(String newUpdateType) 
	  { updateType = newUpdateType; 
	  }
	  
	  public String getSerialNo() 
	  { return serialNo; 
	  }
	  public void setSerialNo(String newSerialNo) 
	  { serialNo = newSerialNo; 
	  } 
	  
	  public String getStatus() 
	  { return status; 
	  }
	  public void setStatus(String newStatus)
	  { status = newStatus; 
	  } 
	  public String getEnggCode() 
	  { return enggCode; 
	  }
	  public void setEnggCode(String newEnggCode)
	  { enggCode = newEnggCode; 
	  } 
	  public String getROCode() 
	  { return ROCode; 
	  }
	  public void setROCode(String newROCode)
	  { ROCode = newROCode; 
	  } 

}
